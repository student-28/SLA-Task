// write a program to print even and odd numbers using for loop between 1 to 20
console.log("EVEN NUMBERS BETWEEN 1 TO 20 \n");
for(let i=1;i<=20;i++){
  if(i%2==0){
    console.log(i);
  }
}
console.log("\nODD NUMBERS BETWEEN 1 TO 20\n");
for(let i=1;i<=20;i++){
  if(i%2!=0){
    console.log(i);
  }
}
